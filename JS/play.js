let currentSongIndex = 0;
window.addEventListener('scroll', function () {
  var header = document.querySelector('header');
  header.classList.toggle('sticky', window.scrollY > 0);
});
function switchSong() {
  var audioPlayer = document.getElementById('audio-player');
  currentSongIndex = (currentSongIndex + 1) % songs.length;
  audioPlayer.src = songs[currentSongIndex].source;
  updateLyrics(songs[currentSongIndex].lyrics.slice(0, songs[currentSongIndex].lyrics.length / 2),
    songs[currentSongIndex].lyrics.slice(songs[currentSongIndex].lyrics.length / 2));
  updateSongTitle(songs[currentSongIndex].title);
}
function updateLyrics(column1Content, column2Content) {
  var lyricsColumn1 = document.querySelector('.lyrics-column:nth-child(1)');
  var lyricsColumn2 = document.querySelector('.lyrics-column:nth-child(2)');

  lyricsColumn1.innerHTML = column1Content.map(line => `<p>${line}</p>`).join('');
  lyricsColumn2.innerHTML = column2Content.map(line => `<p>${line}</p>`).join('');
}
function updateSongTitle(title) {
  var songTitle = document.getElementById('song-title');
  songTitle.textContent = title;
}